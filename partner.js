const urlParams = new URLSearchParams(window.location.search);
const partner = urlParams.get("partner");

const style = document.createElement("style");
const cssClass = `
  .category {
    color: ${partnersInfo.partners[partner].colors.colorPartner};
  }
  .category.active {
    background-color: ${partnersInfo.partners[partner].colors.colorPartner};
    color: white;
    box-shadow: 0px 4px 8px rgba(0, 0, 0, 0.2);
  }
`;

style.appendChild(document.createTextNode(cssClass));
document.head.appendChild(style);

let categorySelector = document.querySelector(".category-selector");

document.querySelector(".partner-logo-banner").style.backgroundColor =
  partnersInfo.partners[partner].colors.banner;
document.querySelector(".content-description h1").style.color =
  partnersInfo.partners[partner].colors.colorPartner;

partnersInfo.partners[partner].buttons.forEach((button, index) => {
  categorySelector.insertAdjacentHTML(
    "beforeend",
    `<div class="category ${index === 0 ? "active" : ""}">${button[0]}</div>`
  );
});

let categories = document.querySelectorAll(".category");
categories.forEach((category, index) => {
  category.innerText = `${partnersInfo.partners[partner].buttons[index][0]}`;
});

document.querySelector(".image-container img").src =
  partnersInfo.partners[`${partner}`].buttons[0][4];
document.querySelector(".content-description h1").innerText =
  partnersInfo.partners[`${partner}`].buttons[0][1];
document.querySelector(".content-description h3").innerText =
  partnersInfo.partners[`${partner}`].buttons[0][2];
document.querySelector(".content-description p").innerText =
  partnersInfo.partners[`${partner}`].buttons[0][3];

const partnerLogoBanner = document.querySelector(".partner-logo-banner img");
partnerLogoBanner.src = partnersInfo.partners[`${partner}`].logo;

categories.forEach((category, index) => {
  category.addEventListener("click", (event) => {
    event.preventDefault();
    const previousActiveCategory = document.querySelector(".category.active");
    previousActiveCategory.classList.remove("active");
    category.classList.add("active");

    document.querySelector(".image-container img").src =
      partnersInfo.partners[`${partner}`].buttons[index][4];
    document.querySelector(".content-description h1").innerText =
      partnersInfo.partners[`${partner}`].buttons[index][1];
    document.querySelector(".content-description h3").innerText =
      partnersInfo.partners[`${partner}`].buttons[index][2];
    document.querySelector(".content-description p").innerText =
      partnersInfo.partners[`${partner}`].buttons[index][3];
    console.log(imageContainer);
  });
});
